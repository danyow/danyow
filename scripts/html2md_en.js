              //   return rep.toLocaleLowerCase() + '-'
              // })
              // // 首字母为数字或者大写且后面大写
              // tag = tag.replaceAll(/([0-9]|[A-Z])[A-Z]+/g, function (rep) {
              //   return rep.toLocaleLowerCase() + '-'
